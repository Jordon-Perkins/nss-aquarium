export const mostHolyFish = () => {
    // 3, 6, 9, 12, etc... fish
    const holyFish = []

    for (const fishes of database.fish) {
        if (fishes.length % 3 === 0) {
            holyFish.push(fishes)
        }
    }
    return holyFish
}

